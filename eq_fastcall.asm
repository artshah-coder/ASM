%include'io.inc'

eq_fastcall:
	PUSH EBP
	MOV EBP, ESP
	PUSH ESI
	PUSH EDI
	PUSH EBX
	
	XOR EAX, EAX
	TEST ECX, ECX
	JE .L1
	TEST EDX, EDX
	JE .L2
	JNE .L3
.L1:
	TEST EDX, EDX
	JNE .L2
	MOV EAX, 1
	JMP .L2
.L3:
	MOV SI, WORD [ECX]
	CMP SI, WORD [EDX]
	JNE .L2
	MOV ESI, DWORD [ECX + 8]
	MOV EDI, DWORD [EDX + 8]
	MOV ECX, DWORD [ECX + 4]
	NOV EDX, DWORD [EDX + 4]
	SUB ESP, 12
	CALL eq_fastcall
	ADD ESP, 12
	TEST EAX, EAX
	JE .L2
	MOV EBX, EAX
	MOV EDX, EDI
	MOV ECX, ESI
	CALL eq_fastcall
	ADD ESP, 12
	AND EAX, EBX
.L2:
	POP EBX
	POP EDI
	POP ESI
	POP EBP
	RET
